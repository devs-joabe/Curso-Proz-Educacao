# 1. Identificador das Variáveis
# Vamos usar nomes descritivos como identificadores para cada informação

nome_identificador = "Jose Almeida da Silva"
cpf_identificador = "12345678900"
rg_identificador = "9517530"
altura_identificador = 1.78
endereco_identificador = "Rua A, 380, Centro, Recife/PE"

# 2. Declaração das Variáveis com Seus Respectivos Identificadores e Tipos de Dado

nome = str(nome_identificador)
cpf = str(cpf_identificador)
rg = str(rg_identificador)
altura = float(altura_identificador)
endereco = str(endereco_identificador)

# 3. Utilização do Comando de Atribuição, Apresentando Identificador e Dado

print("Em uma inscricao, o usuario informou os seguintes dados:")
print("- Nome:", nome)
print("- CPF:", cpf)
print("- RG:", rg)
print("- Altura:", altura)
print("- Endereco:", endereco)
